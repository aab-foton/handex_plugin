// Regenerate via: node src/plugin/refs/build-skeleton.cjs
const skeletonPath = path.join(BASE, 'refs', '_skeleton.json');
let refSkeletonJSON = 'null';
if (fs.existsSync(skeletonPath)) {
  const raw = fs.readFileSync(skeletonPath, 'utf8');
  const parsed = JSON.parse(raw);
  refSkeletonJSON = JSON.stringify(parsed);

  // Sanity-check against characters that break the JS string literal
  // Figma wraps ui.html in: U+2028 LINE SEP, U+2029 PARA SEP.
  // Built via String.fromCharCode so this file stays free of them.
  const u2028 = String.fromCharCode(0x2028);
  const u2029 = String.fromCharCode(0x2029);
  if (refSkeletonJSON.indexOf(u2028) >= 0 || refSkeletonJSON.indexOf(u2029) >= 0) {
    throw new Error('⛔  _skeleton.json contains U+2028/U+2029 — regenerate via build-skeleton.cjs');
  }
} else {
  console.warn('⚠  refs/_skeleton.json not found — run: node src/plugin/refs/build-skeleton.cjs');
}

// ── Load Code Connect mappings (lib name → import path / docs) ──
const codeMapPath = path.join(BASE, 'refs', 'code-mappings.json');
let codeMappingsJSON = 'null';
if (fs.existsSync(codeMapPath)) {
  const raw = fs.readFileSync(codeMapPath, 'utf8');
  const parsed = JSON.parse(raw);
  codeMappingsJSON = JSON.stringify(parsed);
  const u2028 = String.fromCharCode(0x2028);
  const u2029 = String.fromCharCode(0x2029);
  if (codeMappingsJSON.indexOf(u2028) >= 0 || codeMappingsJSON.indexOf(u2029) >= 0) {
    throw new Error('⛔  code-mappings.json contains illegal U+2028/U+2029 whitespace');
  }
} else {
  console.warn('⚠  refs/code-mappings.json not found — Code Connect column will be empty');
}

